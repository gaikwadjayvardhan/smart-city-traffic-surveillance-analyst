# ---------------------------------------------------------------------------
# Action
# ---------------------------------------------------------------------------
VALID_ACTIONS = {"tag_collision", "tag_near_miss", "dispatch_ems", "update_signs", "advance_frame", "noop"}

class TrafficAction(_Action):
    """
    Polymorphic action the agent can submit.

    action_type values:
      • tag_collision    — mark a collision event
      • tag_near_miss    — mark a near-miss event
      • dispatch_ems     — send ambulance/fire to coordinates
      • update_signs     — change upstream VMS sign messages
      • advance_frame    — move to the next camera frame (no side effect)
      • noop             — do nothing (penalised mildly)

    payload keys vary by action_type:
      tag_collision  : {frame_id: int, vehicle_ids: List[str]}
      tag_near_miss  : {vehicle_ids: List[str], minimum_distance: float}
      dispatch_ems   : {coordinates: [lat, lon], service_type: str}
      update_signs   : {sign_ids: List[str], message: str, upstream: bool}
      advance_frame  : {}
      noop           : {}
    """

    action_type: str = Field(..., description=f"One of {VALID_ACTIONS}")
    payload: Dict[str, Any] = Field(default_factory=dict)

    @model_validator(mode="after")
    def validate_action_type(self) -> "TrafficAction":
        if self.action_type not in VALID_ACTIONS:
            raise ValueError(
                f"action_type '{self.action_type}' is not valid. "
                f"Must be one of {VALID_ACTIONS}"
            )
        return self


