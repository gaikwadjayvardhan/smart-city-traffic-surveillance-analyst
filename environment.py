class RewardEngine:
    """
    Computes dense, continuous reward signals (never purely sparse).

    Reward philosophy:
      +1.0  — perfect action (right type, right payload, right sequence)
      +0.5  — partial credit (right type, mostly right payload)
      -0.1  — non-destructive wrong action (noop, wrong frame)
      -0.3  — hallucinated vehicle IDs that don't exist in the episode
      -0.5  — repeat action already submitted correctly
    """

    def __init__(self, ground_truth: Dict[str, Any], all_vehicle_ids: List[str]):
        self.gt = ground_truth
        self.all_vehicle_ids = set(all_vehicle_ids)
        self._collision_tagged = False
        self._ems_dispatched = False
        self._signs_updated = False

    def score(self, action: TrafficAction, state: TrafficState) -> float:
        at = action.action_type
        p = action.payload

        if at == "noop":
            return -0.05  # mild penalty for doing nothing

        if at == "advance_frame":
            return 0.0  # neutral

        # Check for hallucinated vehicle IDs
        submitted_ids: List[str] = p.get("vehicle_ids", [])
        hall = [v for v in submitted_ids if v not in self.all_vehicle_ids]
        if hall:
            return -0.3  # hallucination penalty

        if at == "tag_collision":
            if self._collision_tagged:
                return -0.5  # duplicate
            submitted_frame = p.get("frame_id")
            submitted_vids = set(p.get("vehicle_ids", []))
            gt_frame = self.gt["collision_frame"]
            gt_vids = set(self.gt["collision_vehicle_ids"])

            frame_ok = submitted_frame == gt_frame
            vids_ok = submitted_vids == gt_vids

            if frame_ok and vids_ok:
                self._collision_tagged = True
                return 1.0
            elif frame_ok or vids_ok:
                return 0.5  # partial credit
            else:
                return -0.1

        if at == "tag_near_miss":
            submitted_vids = set(p.get("vehicle_ids", []))
            submitted_dist = p.get("minimum_distance", None)
            gt_vid = self.gt["near_miss_vehicle_id"]
            gt_dist = self.gt["near_miss_min_distance_m"]

            vid_ok = gt_vid in submitted_vids
            dist_ok = (
                submitted_dist is not None
                and abs(submitted_dist - gt_dist) <= 1.5  # 1.5m tolerance
            )
            if vid_ok and dist_ok:
                return 1.0
            elif vid_ok:
                return 0.4
            else:
                return -0.1

        if at == "dispatch_ems":
            if self._ems_dispatched:
                return -0.5
            coords = p.get("coordinates", [])
            if len(coords) != 2:
                return -0.1
            gt_lat, gt_lon = self.gt["collision_coordinates"]
            dist_m = _haversine_m(coords[0], coords[1], gt_lat, gt_lon)
            service = p.get("service_type", "")

            coord_ok = dist_m < 200  # within 200 m
            service_ok = service in ("ambulance", "fire", "police", "ems")

            if coord_ok and service_ok:
                self._ems_dispatched = True
                return 1.0
            elif coord_ok:
                return 0.5
            else:
                return -0.1

        if at == "update_signs":
            if self._signs_updated:
                return -0.5
            sign_ids = set(p.get("sign_ids", []))
            gt_signs = set(self.gt["sign_ids"])
            message = p.get("message", "")
            upstream = p.get("upstream", False)

            sign_ok = bool(sign_ids & gt_signs)  # at least one correct sign
            msg_ok = len(message) > 3  # non-empty reasonable message
            up_ok = upstream is True

            if sign_ok and msg_ok and up_ok:
                self._signs_updated = True
                return 1.0
            elif sign_ok and msg_ok:
                return 0.5
            elif msg_ok:
                return 0.2
            else:
                return -0.1

        return -0.1  # unknown action type fallback


