class TrafficSurveillanceEnv(_Environment):
    """
    Smart City Traffic Surveillance Analyst — OpenEnv Environment.

    Episode lifecycle:
      1. reset(task_name=...) → generates a fresh scenario, returns obs
      2. Agent calls step(action) repeatedly (advance_frame + tag/dispatch)
      3. Episode ends when agent completes required actions or max_steps hit

    Validator contract:
      ✓ reset() returns TrafficObservation
      ✓ step()  returns TrafficObservation with .reward and .done
      ✓ state   is a @property returning TrafficState
    """

    MAX_STEPS_PER_EPISODE = 60

    def __init__(self):
        self._state: TrafficState = TrafficState()
        self._scenario: Dict[str, Any] = {}
        self._reward_engine: Optional[RewardEngine] = None
        self._rng = random.Random()

    # ------------------------------------------------------------------
    # reset()
    # ------------------------------------------------------------------
    def reset(
        self,
        seed: Optional[int] = None,
        episode_id: Optional[str] = None,
        task_name: Optional[str] = None,
        **kwargs: Any,
    ) -> TrafficObservation:
        """
        Reset the environment to a clean initial state.

        Args:
            seed:       RNG seed for reproducibility.
            episode_id: Optional identifier for this episode.
            task_name:  One of 'collision_tagging', 'near_miss_detection',
                        'active_incident_management'. Defaults to 'collision_tagging'.

        Returns:
            TrafficObservation at frame 0.
        """
        # Deterministic seed
        if seed is not None:
            self._rng = random.Random(seed)
        else:
            self._rng = random.Random()

        task_name = task_name or "collision_tagging"
        ep_id = episode_id or f"ep_{int(time.time())}"

        # Generate scenario
        gen = ScenarioGenerator(self._rng)
        self._scenario = gen.generate_episode(total_frames=30, n_vehicles=6)
        gt = self._scenario["ground_truth"]

        # All vehicle IDs in this episode
        all_vids = list({
            v["vehicle_id"]
            for frame in self._scenario["frames"]
            for v in frame
        })

        # Fresh reward engine
        self._reward_engine = RewardEngine(ground_truth=gt, all_vehicle_ids=all_vids)

        # Clean state
        self._state = TrafficState(
            step_count=0,
            task_name=task_name,
            episode_id=ep_id,
            current_frame=0,
            total_frames=self._scenario["total_frames"],
            cumulative_reward=0.0,
            last_action=None,
            tagged_collisions=[],
            tagged_near_misses=[],
            dispatched_ems=[],
            updated_signs=[],
            active_alerts=[],
            ground_truth=gt,
            episode_done=False,
        )

        return self._build_observation(reward=0.0, done=False)

    # ------------------------------------------------------------------
    # step()
    # ------------------------------------------------------------------
    def step(
        self,
        action: TrafficAction,
        timeout_s: Optional[float] = None,
        **kwargs: Any,
    ) -> TrafficObservation:
        """
        Apply an action and advance the environment.

        Returns TrafficObservation with .reward (dense signal) and .done.
        """
        if self._reward_engine is None:
            raise RuntimeError("Call reset() before step().")

        self._state.step_count += 1
        self._state.last_action = action.model_dump()

        # ------------------------------------------------------------------
        # Compute reward
        # ------------------------------------------------------------------
        reward = self._reward_engine.score(action, self._state)
        self._state.cumulative_reward += reward

        # ------------------------------------------------------------------
        # Apply side effects of action
        # ------------------------------------------------------------------
        at = action.action_type
        p = action.payload

        if at == "advance_frame":
            if self._state.current_frame < self._state.total_frames - 1:
                self._state.current_frame += 1

        elif at == "tag_collision":
            alert = Alert(
                alert_id=f"COLL_{self._state.step_count}",
                alert_type="collision",
                frame_id=p.get("frame_id"),
                vehicle_ids=p.get("vehicle_ids", []),
                coordinates=self._state.ground_truth.get("collision_coordinates"),
            )
            self._state.tagged_collisions.append(alert.model_dump())
            self._state.active_alerts.append(alert.model_dump())

        elif at == "tag_near_miss":
            alert = Alert(
                alert_id=f"NM_{self._state.step_count}",
                alert_type="near_miss",
                vehicle_ids=p.get("vehicle_ids", []),
                details={"minimum_distance": p.get("minimum_distance")},
            )
            self._state.tagged_near_misses.append(alert.model_dump())
            self._state.active_alerts.append(alert.model_dump())

        elif at == "dispatch_ems":
            alert = Alert(
                alert_id=f"EMS_{self._state.step_count}",
                alert_type="ems_dispatched",
                coordinates=tuple(p.get("coordinates", [0.0, 0.0])),
                details={"service_type": p.get("service_type", "ambulance")},
            )
            self._state.dispatched_ems.append(alert.model_dump())
            self._state.active_alerts.append(alert.model_dump())

        elif at == "update_signs":
            alert = Alert(
                alert_id=f"SIGN_{self._state.step_count}",
                alert_type="sign_updated",
                details={
                    "sign_ids": p.get("sign_ids", []),
                    "message": p.get("message", ""),
                    "upstream": p.get("upstream", False),
                },
            )
            self._state.updated_signs.append(alert.model_dump())
            self._state.active_alerts.append(alert.model_dump())

        # ------------------------------------------------------------------
        # Check episode termination
        # ------------------------------------------------------------------
        done = self._check_done()
        self._state.episode_done = done

        return self._build_observation(reward=reward, done=done)

    # ------------------------------------------------------------------
    # state (property)
    # ------------------------------------------------------------------
    @property
    def state(self) -> TrafficState:
        """Return current internal state snapshot. No parentheses needed."""
        return deepcopy(self._state)

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------
    def _build_observation(self, reward: float, done: bool) -> TrafficObservation:
        """Construct observation from current state."""
        frame_idx = self._state.current_frame
        raw_frame = self._scenario.get("frames", [[]])[frame_idx] if self._scenario else []

        telemetry = [VehicleTelemetry(**v) for v in raw_frame]
        alerts = [Alert(**a) for a in self._state.active_alerts]

        return TrafficObservation(
            done=done,
            reward=reward,
            current_frame=frame_idx,
            total_frames=self._state.total_frames,
            telemetry_data=telemetry,
            active_alerts=alerts,
            episode_id=self._state.episode_id,
            task_name=self._state.task_name,
        )

    def _check_done(self) -> bool:
        """
        Episode is done when:
          - Max steps exceeded, OR
          - For active_incident_management: all 3 sub-tasks completed, OR
          - The frame pointer reaches the end AND the task-specific condition is met
        """
        if self._state.step_count >= self.MAX_STEPS_PER_EPISODE:
            return True

        task = self._state.task_name
        if task == "collision_tagging":
            return len(self._state.tagged_collisions) > 0
        elif task == "near_miss_detection":
            return len(self._state.tagged_near_misses) > 0
        elif task == "active_incident_management":
            return (
                len(self._state.tagged_collisions) > 0
                and len(self._state.dispatched_ems) > 0
                and len(self._state.updated_signs) > 0
            )
        return False

    def get_all_vehicle_ids(self) -> List[str]:
        """Utility: return all vehicle IDs present in this episode."""
        if not self._scenario:
            return []
        return list({
            v["vehicle_id"]
            for frame in self._scenario["frames"]
            for v in frame
        })

    def get_frame(self, frame_idx: int) -> List[Dict]:
        """Return raw telemetry dict list for a specific frame."""
        if not self._scenario or frame_idx >= len(self._scenario["frames"]):
            return []
        return self._scenario["frames"][frame_idx]

    def get_ground_truth(self) -> Dict[str, Any]:
        """Expose ground truth (for graders — do NOT expose to agent)."""
        return deepcopy(self._scenario.get("ground_truth", {}))


