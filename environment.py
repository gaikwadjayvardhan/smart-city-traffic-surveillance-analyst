def _haversine_m(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Return distance in metres between two GPS points."""
    R = 6_371_000.0
    phi1, phi2 = math.radians(lat1), math.radians(lat2)
    dphi = math.radians(lat2 - lat1)
    dlam = math.radians(lon2 - lon1)
    a = math.sin(dphi / 2) ** 2 + math.cos(phi1) * math.cos(phi2) * math.sin(dlam / 2) ** 2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def _bbox_overlap(b1: Tuple, b2: Tuple) -> bool:
    """Return True if two bounding boxes overlap."""
    return not (b1[2] < b2[0] or b2[2] < b1[0] or b1[3] < b2[1] or b2[3] < b1[1])


class ScenarioGenerator:
    """
    Generates reproducible multi-frame traffic scenarios including:
      - normal traffic flow
      - a hidden collision event at a randomised frame
      - a near-miss trajectory
    """

    BASE_LAT = 37.7749   # San Francisco
    BASE_LON = -122.4194

    def __init__(self, rng: random.Random):
        self.rng = rng

    def _random_gps(self, spread: float = 0.005) -> Tuple[float, float]:
        return (
            self.BASE_LAT + self.rng.uniform(-spread, spread),
            self.BASE_LON + self.rng.uniform(-spread, spread),
        )

    def _vehicle_id(self, n: int) -> str:
        return f"V{n:03d}"

    def generate_episode(
        self,
        total_frames: int = 30,
        n_vehicles: int = 6,
        collision_frame: Optional[int] = None,
    ) -> Dict[str, Any]:
        """
        Returns a full episode dict with:
          - frames: List[List[VehicleTelemetry]]
          - ground_truth: {collision_frame, collision_vehicles, near_miss_vehicle, min_distance, ...}
        """
        if collision_frame is None:
            collision_frame = self.rng.randint(5, total_frames - 5)

        # Assign stable base positions and headings to each vehicle
        vehicle_configs = []
        for i in range(1, n_vehicles + 1):
            lat, lon = self._random_gps()
            speed = self.rng.uniform(30, 90)
            heading = self.rng.uniform(0, 360)
            vehicle_configs.append(
                {"id": self._vehicle_id(i), "lat": lat, "lon": lon, "speed": speed, "heading": heading}
            )

        # Identify collision pair and near-miss vehicle
        coll_v1_idx, coll_v2_idx = 0, 1
        near_miss_idx = 2

        # Near-miss: vehicle 3 drifts close to vehicle 4 around frame 15
        near_miss_frame = collision_frame - 5 if collision_frame > 10 else collision_frame + 5
        near_miss_min_dist = round(self.rng.uniform(3.5, 8.0), 2)  # metres

        frames: List[List[Dict]] = []

        for f in range(total_frames):
            frame_vehicles: List[Dict] = []
            for cfg in vehicle_configs:
                vid = cfg["id"]
                # Normal progression: advance lat/lon by speed
                progress = f * 0.00001 * cfg["speed"] / 50
                lat = cfg["lat"] + progress * math.cos(math.radians(cfg["heading"]))
                lon = cfg["lon"] + progress * math.sin(math.radians(cfg["heading"]))
                speed = cfg["speed"]

                # Simulate collision: V001 and V002 converge and stop at collision_frame
                if vid in (
                    vehicle_configs[coll_v1_idx]["id"],
                    vehicle_configs[coll_v2_idx]["id"],
                ):
                    if f < collision_frame:
                        # Approach phase
                        frac = f / collision_frame
                        target_lat = (
                            vehicle_configs[coll_v1_idx]["lat"]
                            + vehicle_configs[coll_v2_idx]["lat"]
                        ) / 2
                        target_lon = (
                            vehicle_configs[coll_v1_idx]["lon"]
                            + vehicle_configs[coll_v2_idx]["lon"]
                        ) / 2
                        lat = cfg["lat"] + frac * (target_lat - cfg["lat"])
                        lon = cfg["lon"] + frac * (target_lon - cfg["lon"])
                    elif f == collision_frame:
                        # IMPACT: both at same point, speed drops to 0
                        lat = (
                            vehicle_configs[coll_v1_idx]["lat"]
                            + vehicle_configs[coll_v2_idx]["lat"]
                        ) / 2
                        lon = (
                            vehicle_configs[coll_v1_idx]["lon"]
                            + vehicle_configs[coll_v2_idx]["lon"]
                        ) / 2
                        speed = 0.0
                    else:
                        # Post-collision: stationary
                        lat = (
                            vehicle_configs[coll_v1_idx]["lat"]
                            + vehicle_configs[coll_v2_idx]["lat"]
                        ) / 2
                        lon = (
                            vehicle_configs[coll_v1_idx]["lon"]
                            + vehicle_configs[coll_v2_idx]["lon"]
                        ) / 2
                        speed = 0.0

                # Near miss adjustment for vehicle[near_miss_idx]
                if vid == vehicle_configs[near_miss_idx]["id"]:
                    anchor_v = vehicle_configs[near_miss_idx + 1]
                    if f == near_miss_frame:
                        # Temporarily bring within near_miss_min_dist
                        offset_lat = near_miss_min_dist / 111_000
                        lat = anchor_v["lat"] + offset_lat
                        lon = anchor_v["lon"]
                        speed = self.rng.uniform(15, 30)

                # Build bbox around GPS point (pixel proxy: scale gps diff to pixels)
                px = (lon - self.BASE_LON) * 100_000
                py = (lat - self.BASE_LAT) * 100_000
                w, h = 40.0, 20.0
                bbox = (px - w / 2, py - h / 2, px + w / 2, py + h / 2)

                frame_vehicles.append({
                    "vehicle_id": vid,
                    "bbox": bbox,
                    "speed_kmh": round(speed, 2),
                    "coordinates": (round(lat, 7), round(lon, 7)),
                    "heading_deg": round(cfg["heading"], 2),
                })

            frames.append(frame_vehicles)

        # Compute actual collision GPS
        collision_lat = (
            vehicle_configs[coll_v1_idx]["lat"] + vehicle_configs[coll_v2_idx]["lat"]
        ) / 2
        collision_lon = (
            vehicle_configs[coll_v1_idx]["lon"] + vehicle_configs[coll_v2_idx]["lon"]
        ) / 2

        # Compute near-miss: anchor vehicle position at near_miss_frame
        anchor = vehicle_configs[near_miss_idx + 1]
        nm_v = vehicle_configs[near_miss_idx]
        anchor_lat = anchor["lat"]
        anchor_lon = anchor["lon"]
        offset_lat = near_miss_min_dist / 111_000
        nm_lat = anchor_lat + offset_lat
        nm_lon = anchor_lon
        actual_dist = round(
            _haversine_m(nm_lat, nm_lon, anchor_lat, anchor_lon), 2
        )

        ground_truth = {
            "collision_frame": collision_frame,
            "collision_vehicle_ids": [
                vehicle_configs[coll_v1_idx]["id"],
                vehicle_configs[coll_v2_idx]["id"],
            ],
            "collision_coordinates": (round(collision_lat, 7), round(collision_lon, 7)),
            "near_miss_vehicle_id": vehicle_configs[near_miss_idx]["id"],
            "near_miss_anchor_vehicle_id": vehicle_configs[near_miss_idx + 1]["id"],
            "near_miss_frame": near_miss_frame,
            "near_miss_min_distance_m": actual_dist,
            "sign_ids": ["SIGN_N1", "SIGN_N2"],  # Upstream signs to update
        }

        return {"frames": frames, "ground_truth": ground_truth, "total_frames": total_frames}


